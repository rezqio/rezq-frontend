export const getAnimal = (s) => {
  const hash = Math.abs(String(s).hashCode());
  return `${ADJECTIVES[hash % ADJECTIVES.length]} ${ANIMALS[hash % ANIMALS.length]}`;
};

export const getProfileLinkOrAnimal = profile => (profile.username ? (
  <a href={`/profile/${profile.username}`}>
    {profile.username}
  </a>
) : (
  <span>
    {getAnimal(profile.id)}
  </span>
));
